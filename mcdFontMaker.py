bytemessage = bytearray()

for fontEntry in font['fonts']:
    print("ID:",fontEntry['id'])
    # ID da fonte - Uma vez por fontEntry
    idFonte =fontEntry['id'] #int
    idFonteByte = idFonte.to_bytes(2, 'little')

    for charEntry in fontEntry['symbols']:
        print("Escrevendo:",charEntry['char'],end='\n')
        # Bytes do ID da fonte
        for byte in idFonteByte:
            bytemessage.append(byte)

        # Char da fonte
        charInt = ord(charEntry['char'])
        charByte = charInt.to_bytes(2, 'little')
        for byte in charByte:
            bytemessage.append(byte)

        # ID do char da fonte
        idCharInt = charEntry['glyph_id']
        idCharByte = idCharInt.to_bytes(2, 'little')
        for byte in idCharByte:
            bytemessage.append(byte)

        # Bytes de término de string
        for _ in range(2):
            bytemessage.append(0)

    print()

with open("font.mcd", "wb") as binary_file:
    binary_file.write(bytemessage)
        

